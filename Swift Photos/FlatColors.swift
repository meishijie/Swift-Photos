import UIKit
import UIColor_Hex_Swift

// Color values are taken from iOS8Colors ( https://github.com/thii/iOS8Colors/ ).

extension UIColor {
    // MARK: - Colors
    class func flatRedColor() -> UIColor { return UIColor(rgba: "#FF3B30") }
    class func flatOrangeColor() -> UIColor { return UIColor(rgba: "#FF9500") }
    class func flatYellowColor() -> UIColor { return UIColor(rgba: "#FFCC00") }
    class func flatGreenColor() -> UIColor { return UIColor(rgba: "#4CD964") }
    class func flatLightBlueColor() -> UIColor { return UIColor(rgba: "#34AADC") }
    class func flatDarkBlueColor() -> UIColor { return UIColor(rgba: "#007AFF") }
    class func flatPurpleColor() -> UIColor { return UIColor(rgba: "#5856D6") }
    class func flatPinkColor() -> UIColor { return UIColor(rgba: "#FF2D55") }
    class func flatDarkGrayColor() -> UIColor { return UIColor(rgba: "#8E8E93") }
    class func flatLightGrayColor() -> UIColor { return UIColor(rgba: "#C7C7CC") }
    // MARK: - Gradients
    class func flatRedGradientStartColor() -> UIColor { return UIColor(rgba: "#FF5E3A") }
    class func flatRedGradientEndColor() -> UIColor { return UIColor(rgba: "#FF2A68") }
    class func flatOrangeGradientStartColor() -> UIColor { return UIColor(rgba: "#FF9500") }
    class func flatOrangeGradientEndColor() -> UIColor { return UIColor(rgba: "#FF5E3A") }
    class func flatYellowGradientStartColor() -> UIColor { return UIColor(rgba: "#FFDB4C") }
    class func flatYellowGradientEndColor() -> UIColor { return UIColor(rgba: "#FFCD02") }
    class func flatGreenGradientStartColor() -> UIColor { return UIColor(rgba: "#87FC70") }
    class func flatGreenGradientEndColor() -> UIColor { return UIColor(rgba: "#0BD318") }
    class func flatTealGradientStartColor() -> UIColor { return UIColor(rgba: "#52EDC7") }
    class func flatTealGradientEndColor() -> UIColor { return UIColor(rgba: "#5AC8FB") }
    class func flatBlueGradientStartColor() -> UIColor { return UIColor(rgba: "#1AD6FD") }
    class func flatBlueGradientEndColor() -> UIColor { return UIColor(rgba: "#1D62F0") }
    class func flatVioletGradientStartColor() -> UIColor { return UIColor(rgba: "#C644FC") }
    class func flatVioletGradientEndColor() -> UIColor { return UIColor(rgba: "#5856D6") }
    class func flatMagentaGradientStartColor() -> UIColor { return UIColor(rgba: "#EF4DB6") }
    class func flatMagentaGradientEndColor() -> UIColor { return UIColor(rgba: "#C643FC") }
    class func flatBlackGradientStartColor() -> UIColor { return UIColor(rgba: "#4A4A4A") }
    class func flatBlackGradientEndColor() -> UIColor { return UIColor(rgba: "#2B2B2B") }
    class func flatSilverGradientStartColor() -> UIColor { return UIColor(rgba: "#DBDDDE") }
    class func flatSilverGradientEndColor() -> UIColor { return UIColor(rgba: "#898C90") }
}
